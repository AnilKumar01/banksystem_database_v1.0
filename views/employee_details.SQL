CREATE VIEW EMP_DETAIL AS 
SELECT EMPLOYEE_ID ,CONCAT(FIRST_NAME ,' ',Middle_NAME,' ',LAST_NAME) AS NAME , 
GENDER, DATE_OF_BIRTH,Parmanent_Address,CURRENT_ADDRESS, CONTACT_NO,E_MAIL,
EMERGENCY_CONTACT_NO,HIGHEST_QUALIFICATION,DESIGNATION,DATE_OF_JOINING, DEPARTMENT_NAME,
CONCAT(ADDRESS,', ',LOCATION,', Phone: ',TELEPHONE_NO,', FAX: ',FAX_NO,',IFSC Code: ',IFSC_CODE,', Branch Code: ',BRANCH_LIST.BRANCH_CODE) AS ADDRESS 
FROM Employee,DEPARTMENT,BRanch_list
WHERE Employee.DEPARTMENT_NO=DEPARTMENT.DEPARTMENT_NO 
AND EMPLOYEE.BRANCH_CODE = BRanch_list.BRANCH_CODE;