CREATE VIEW EMPLOYEE_SALARY_SLIP AS
SELECT e.Employee_Id, CONCAT(FIRST_NAME,' ',MIDDLE_NAME,' ',LAST_NAME) AS NAME, GENDER, DATE_OF_BIRTH,
CONTACT_NO,E_MAIL,DESIGNATION, DATE_OF_JOINING,d.DEPARTMENT_NAME,b.BRANCH_CODE,LOCATION ,
basic,hra,da,ta,oa,pf,ma,TOTAL_SALARY
FROM employee e,branch_list b,department d ,employee_salary sal
WHERE e.DEPARTMENT_NO=d.DEPARTMENT_NO 
AND e.BRANCH_CODE= b.BRANCH_CODE
AND e.EMPLOYEE_ID= sal.EMPLOYEE_ID;