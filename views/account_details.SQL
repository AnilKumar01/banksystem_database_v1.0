USE BANK_SYSTEM

DROP VIEW IF EXISTS ACCOUNT_DETAIL;
CREATE VIEW ACCOUNT_DETAIL
AS
SELECT A.ACCOUNT_NO, A.ACCOUNT_TYPE, A.NET_BANKING, A.DEBIT_CARD, A.CREDIT_CARD, CONCAT(C.FIRST_NAME, " ", C.MIDDLE_NAME, " ", C.LAST_NAME) ACCOUNT_HOLDER, C.MOBILE_NO, C.CURRENT_ADDRESS, B.BRANCH_CODE, B.IFSC_CODE, B.LOCATION, B.ADDRESS, B.TELEPHONE_NO, B.FAX_NO
FROM ACCOUNT A, CUSTOMER C, CUSTOMER_ACCOUNT_RELATION CAR, BRANCH_LIST B, ACCOUNT_BRANCH_RELATION ABR
WHERE A.ACCOUNT_NO=CAR.ACCOUNT_NO AND CAR.CUSTOMER_ID=C.CUSTOMER_ID AND A.ACCOUNT_NO=ABR.ACCOUNT_NO AND ABR.BRANCH_CODE=B.BRANCH_CODE;
