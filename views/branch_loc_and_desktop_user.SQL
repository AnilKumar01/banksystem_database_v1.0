USE BANK_SYSTEM

DROP VIEW IF EXISTS BRANCH_LOCATIONS;
CREATE VIEW BRANCH_LOCATIONS
AS
SELECT BRANCH_CODE, LOCATION FROM BRANCH_LIST
ORDER BY LOCATION;

DROP VIEW IF EXISTS DESKTOP_USERS;
CREATE VIEW DESKTOP_USERS
AS
SELECT USERNAME, AES_DECRYPT(PASSWORD,"6575") PASSWORD, ACCOUNT_LEVEL, ACCOUNT_STATUS
FROM DESKTOP_APP_USERS; 

