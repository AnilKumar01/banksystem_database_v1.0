DROP VIEW IF EXISTS WITHDRAWL_VIEW;
CREATE VIEW WITHDRAWL_VIEW AS 
Select acc.ACCOUNT_NO, acc.ACCOUNT_BALANCE,
cust.First_NAME,cust.MIDDLE_NAME,cust.LAST_NAME,cust.GENDER,
cust.PAN_CARD_NO,cust.PHOTOGRAPH,cust.SIGNATURE
FROM
ACCOUNT acc, CUSTOMER cust , CUSTOMER_ACCOUNT_RELATION rel 
WHERE acc.ACCOUNT_NO=rel.ACCOUNT_NO AND rel.CUSTOMER_ID= cust.CUSTOMER_ID;
