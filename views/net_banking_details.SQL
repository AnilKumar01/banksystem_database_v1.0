USE BANK_SYSTEM;

DROP VIEW IF EXISTS NET_BK_DETAILS;
CREATE VIEW NET_BK_DETAILS
AS
SELECT A.ACCOUNT_NO, A.ACCOUNT_TYPE, CONCAT(FIRST_NAME, " ", MIDDLE_NAME, " ", LAST_NAME) ACCOUNT_HOLDER, A.NET_BANKING
FROM ACCOUNT A, CUSTOMER C, CUSTOMER_ACCOUNT_RELATION CAR
WHERE A.ACCOUNT_NO=CAR.ACCOUNT_NO AND CAR.CUSTOMER_ID=C.CUSTOMER_ID;
