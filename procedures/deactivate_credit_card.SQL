USE BANK_SYSTEM

DELIMITER //

DROP PROCEDURE IF EXISTS DEACTIVATE_CREDIT_CARD;
CREATE PROCEDURE DEACTIVATE_CREDIT_CARD
(
	IN P_ACCOUNT_NO VARCHAR(20)	
)
BEGIN
	UPDATE CREDIT_CARD
	SET DEACTIVATION_DATE=NOW()
	WHERE ACCOUNT_NO=P_ACCOUNT_NO;
		
	UPDATE ACCOUNT
	SET CREDIT_CARD="NO"
	WHERE ACCOUNT_NO=P_ACCOUNT_NO;
END;
