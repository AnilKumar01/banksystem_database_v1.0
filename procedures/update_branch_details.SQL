USE BANK_SYSTEM
DELIMITER //
DROP PROCEDURE IF EXISTS UPDATE_BRANCH_DETAILS;
CREATE PROCEDURE UPDATE_BRANCH_DETAILS
(
	IN P_BRANCH_CODE VARCHAR(60),
	IN P_IFSC_CODE VARCHAR(20),
	IN P_LOCATION VARCHAR(20),
	IN P_ADDRESS VARCHAR(255),
	IN P_TELEPHONE_NO VARCHAR(20),
	IN P_FAX_NO VARCHAR(20)	 
)
BEGIN
	UPDATE BRANCH_LIST 
	SET IFSC_CODE= P_IFSC_CODE, 
	LOCATION= P_LOCATION,
	ADDRESS= P_ADDRESS,
	TELEPHONE_NO= P_TELEPHONE_NO,
	FAX_NO= P_FAX_NO 
	WHERE BRANCH_CODE=P_BRANCH_CODE;
END;
