USE BANK_SYSTEM

DELIMITER //

DROP PROCEDURE IF EXISTS DEACTIVATE_DEBIT_CARD;
CREATE PROCEDURE DEACTIVATE_DEBIT_CARD
(
	IN P_ACCOUNT_NO VARCHAR(20)	
)
BEGIN
	UPDATE DEBIT_CARD
	SET DEACTIVATION_DATE=NOW()
	WHERE ACCOUNT_NO=P_ACCOUNT_NO;
		
	UPDATE ACCOUNT
	SET DEBIT_CARD="NO"
	WHERE ACCOUNT_NO=P_ACCOUNT_NO;
END;
