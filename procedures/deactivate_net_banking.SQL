USE BANK_SYSTEM

DELIMITER //

DROP PROCEDURE IF EXISTS DEACTIVATE_NET_BANKING;
CREATE PROCEDURE DEACTIVATE_NET_BANKING
(
	IN P_ACCOUNT_NO VARCHAR(20)	
)
BEGIN
	UPDATE NET_BANKING_LOGIN
	SET DACTIVATION_DATE=NOW()
	WHERE ACCOUNT_NO=P_ACCOUNT_NO;
		
	UPDATE ACCOUNT
	SET NET_BANKING="NO"
	WHERE ACCOUNT_NO=P_ACCOUNT_NO;
END;
